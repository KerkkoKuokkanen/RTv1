#include "libft.h"

int	ft_isalnum(int a)
{
	int	i;
	int	b;

	i = 0;
	b = 1;
	if ((a >= 65 && a <= 90) || (a >= 97 && a <= 122) || (a >= 48 && a <= 57))
		return (b);
	return (i);
}
